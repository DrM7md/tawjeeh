<?php

namespace Database\Seeders;

use App\Models\PortfolioReviewItem;
use App\Models\PortfolioReviewTemplate;
use Illuminate\Database\Seeder;

/**
 * قالب «استمارة الإشراف على أداء المنسق» — قسم التربية الإسلامية (رمز النموذج ES-ESI-P10-F3).
 * خمسة مجالات؛ لكل مجال عنوانٌ ومؤشرات أداء، وتُكتب الملاحظات والتوصيات وقت التقييم.
 * مُزامِن (idempotent): إعادة التشغيل تُحدّث البنود لتطابق الاستمارة وتحذف الزائد.
 */
class PortfolioTemplateSeeder extends Seeder
{
    public function run(): void
    {
        // إعادة استخدام القالب القائم (إن وُجد) بدل إنشاء قالب مكرّر، وضبطه على الاستمارة الرسمية.
        $template = PortfolioReviewTemplate::where('is_active', true)->orderBy('id')->first()
            ?? PortfolioReviewTemplate::orderBy('id')->first()
            ?? new PortfolioReviewTemplate;

        $template->fill([
            'name' => 'استمارة الإشراف على أداء المنسق',
            'description' => 'استمارة الإشراف على أداء المنسق وفق مجالات الأداء الخمسة',
            'is_active' => true,
        ])->save();

        // [عنوان المجال، مؤشرات الأداء، الدرجة العظمى]
        $items = [
            [
                'الإدارة والقيادة الأكاديمية',
                'يشارك في إعداد الخطط الأكاديميّة السنويّة، ويتابع تنفيذها بما ينعكس أثرها على مخرجات الطلبة الأكاديمية والسلوكية.',
                20,
            ],
            [
                'التعليم والتعلم',
                'يقدّم التغذية الراجعة البنّاءة حول الخطط اليومية، ويقيّم ممارسات المعلّمين الصفيّة في تنفيذ استراتيجيات التعلّم، وتوظيف أدوات التقويم المستمر والوسائل ومصادر التعلّم والأنشطة بأنواعها المختلفة.',
                20,
            ],
            [
                'تقييم تعلّم الطلبة وتقويم أدائهم',
                'يتابع إعداد وتنفيذ كافة أنواع الاختبارات، ويشرف على قراءة وتحليل نتائج الطلبة وإجراءات رفع التحصيل الأكاديمي.',
                20,
            ],
            [
                'تطوير أداء معلمي القسم',
                'يُسهِم في التطوير المهني للقسم بناءً على الاحتياجات التدريبية ومتابعة تطوّر الأداء.',
                20,
            ],
            [
                'الجوانب المهنية',
                'يطوّر أداءه المهني، ويتّخذ القرارات بمهارة، كما يُثري المادة العلمية في تخصّصه، وتتّسم دروسه وأعماله بالدقّة والخلوّ من الأخطاء.',
                20,
            ],
        ];

        foreach ($items as $i => [$text, $indicators, $max]) {
            PortfolioReviewItem::updateOrCreate(
                ['portfolio_review_template_id' => $template->id, 'sort_order' => $i + 1],
                ['criterion_text' => $text, 'indicators' => $indicators, 'max_score' => $max],
            );
        }

        // حذف أي بنود زائدة (من قوالب سابقة) خارج المجالات الخمسة
        $template->items()->where('sort_order', '>', count($items))->delete();
    }
}
